            <h3>Status of child marriage in India</h3>
            <p>More than 40% of the world’s child marriages take place in India. Almost half of all girls here marry before the age of 18 years; 47% of women aged 20 to 24 were married before age 18.</p>
            <p>Between 1992-93 and 2005-06, the incidence of child marriage in India declined by approximately 7%. In some states, however, child marriage prevalence still exceeds 60%, with the highest rates found in Bihar, Jharkhand, Rajasthan and Andhra Pradesh. The problem is worse in rural India as compared to urban areas, with 56 and 29% prevalence respectively.</p>

            <h3>Missing out on education</h3>
            <p>The data from the International Centre for Research on Women’s 2008 study Knot Ready illustrates the link between child marriage and level of education in India. For example, 77% of those women between the ages of 20 and 24 with no education were married before the age of 18 in 2005-06; the figure is 62 and 27% in the case of those with primary education and secondary or more education respectively.</p>
            <p>Under India’s Prohibition of Child Marriage Act of 2006, the legal age of marriage for girls is 18 years and for boys it is 21 years. The law mandates punitive measures against all who perform, permit, or promote child marriage.</p>

            <h3>Study methodology</h3>
            <p>This study is a field-level exploration conducted to understand the factors contributing to child marriage and examine the impact of Plan International’s initiatives to address the causes and consequences of early marriage. The study presents key findings from India, where the study was conducted in Bikaner district in Rajasthan.</p>
            <p>Data was collected from both Plan International and non-Plan International work areas in the district.</p>
            <p>Qualitative methods were applied to gather information, including in-depth interviews, key informant interviews and focus group discussions with a spectrum of stakeholders. These included married and unmarried young men and women, parents, community leaders and government officials.</p>
            <p>Child marriage continues to be the norm for a majority of girls despite high-level awareness of the legal marrying age.</p>
            <p>The study findings demonstrate that child marriage still prevails as a cultural norm in Bikaner, where a majority of girls and many boys are married before they reach their respective legal ages.</p>
            <p>“In our society, girls are married off even before they turn 18 years and boys also are married off before 21.” (Unmarried girl)</p>

            <h3>Most brides 14 to 16 years old</h3>
            <p>Most of the respondents said that marriages often take place when girls are aged between 14 and 16 years of age and extremely few girls are married after they turn 18.</p>
            <p>The decision to marry the girls is usually made by the girl’s father, or other male members of the family, either through seeking marriage alliances or organising the marriages.</p>
            <p>Most of the respondents who shared this information were aware of the legal ages of marriage.</p>
            <p>Girls who are married very early may continue to live with their parents and be sent to their marital home after a few years.</p>
            <p>The study highlights instances where mostly girls who are married off at 12 to 13 years of age continue to reside with their parents. The girl is only sent to her in-laws house in another village after a few years of marriage. However, in some instances the girl is sent to her marital family immediately after marriage if her in-laws insist upon it. This cultural practice is referred to as Gauna in Bikaner.</p>
            <p>“If a girl is small (13 and less) then she is not sent off, but if she is 14-15 years old then she is sent off immediately after marriage. It depends on the in-laws; if they want their daughter-in-law at the time of marriage, then she is sent off immediately.” (Mother of unmarried girl)</p>

            <h3>Inter-generational shifts</h3>
            <p>Clear patterns of inter-generational shifts are observed in the age of marriage for girls.</p>
            <p>Most of the parents said that girls now are not married below the age of 13 in their village, though it used to be a common practice when they were married themselves.</p>
            <p>“We marry a 15 year old girl, and according to us 18 years is old for a girl, so I do not feel that it is right. Our elders used to marry off girls before 13 above that they used to think of it as a sin.” (Mother of unmarried girl)</p>
